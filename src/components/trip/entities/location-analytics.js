const { EntitySchema } = require('typeorm');

const LocationAnalytics = new EntitySchema({
  name: 'LocationAnalytics',
  tableName: 'location_analytics',
  columns: {
    id: {
      type: 'bigint',
      primary: true,
      generated: true, // autogenerated ID
    },
    location_id: {
      type: 'uuid',
      nullable: false,
      comment: 'References the TripLocation for which these metrics are calculated',
    },
    average_rating: {
      type: 'double',
      nullable: true,
      comment: 'Average user rating for this location',
    },
    total_interest_count: {
      type: 'int',
      nullable: true,
      comment: 'Total number of users who showed interest in this location',
    },
    total_travellers_count: {
      type: 'int',
      nullable: true,
      comment: 'Total number of users who traveled to this location',
    },
    created_at: {
      type: 'timestamp',
      createDate: true,
    },
    updated_at: {
      type: 'timestamp',
      updateDate: true,
    },
    updated_by: {
      type: 'bigint',
      nullable: true,
      comment: 'User ID of the person who last updated these metrics',
    },
  },
  relations: {
    tripLocation: {
      target: 'TripLocation',
      type: 'many-to-one',
      joinColumn: { name: 'location_id' },
      onDelete: 'CASCADE',
    },
  },
});

